CREATE TABLE IF NOT EXISTS companies (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(180) NOT NULL,
    plan INTEGER NOT NULL,
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_companies_name ON companies (name);

CREATE TABLE IF NOT EXISTS plan_limits (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(60) NOT NULL UNIQUE,
    max_users INTEGER NOT NULL,
    max_leads INTEGER NOT NULL,
    max_automations INTEGER NOT NULL,
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    name VARCHAR(140) NOT NULL,
    email VARCHAR(180) NOT NULL,
    password_hash TEXT NOT NULL,
    role INTEGER NOT NULL,
    is_active BOOLEAN NOT NULL,
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS ix_users_company_email ON users (company_id, email);

CREATE TABLE IF NOT EXISTS leads (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    name VARCHAR(140) NOT NULL,
    email VARCHAR(180) NULL,
    phone VARCHAR(40) NULL,
    source VARCHAR(100) NOT NULL,
    status INTEGER NOT NULL,
    owner_user_id BIGINT NULL REFERENCES users(id),
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_leads_company_id ON leads (company_id);
CREATE INDEX IF NOT EXISTS ix_leads_company_status ON leads (company_id, status);

CREATE TABLE IF NOT EXISTS customers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    name TEXT NOT NULL,
    email TEXT NULL,
    phone TEXT NULL,
    lead_id BIGINT NULL REFERENCES leads(id),
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_customers_company_id ON customers (company_id);

CREATE TABLE IF NOT EXISTS pipelines (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    name VARCHAR(100) NOT NULL,
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_pipelines_company_id ON pipelines (company_id);

CREATE TABLE IF NOT EXISTS stages (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pipeline_id BIGINT NOT NULL REFERENCES pipelines(id),
    name VARCHAR(100) NOT NULL,
    "order" INTEGER NOT NULL,
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_stages_pipeline_order ON stages (pipeline_id, "order");

CREATE TABLE IF NOT EXISTS deals (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    lead_id BIGINT NOT NULL REFERENCES leads(id),
    stage_id BIGINT NOT NULL REFERENCES stages(id),
    value NUMERIC(18, 2) NOT NULL,
    status INTEGER NOT NULL,
    owner_user_id BIGINT NULL REFERENCES users(id),
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_deals_company_id ON deals (company_id);
CREATE INDEX IF NOT EXISTS ix_deals_company_stage ON deals (company_id, stage_id);

CREATE TABLE IF NOT EXISTS activities (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    deal_id BIGINT NULL REFERENCES deals(id),
    type INTEGER NOT NULL,
    description VARCHAR(500) NOT NULL,
    due_at_utc TIMESTAMPTZ NOT NULL,
    status INTEGER NOT NULL,
    assigned_user_id BIGINT NULL REFERENCES users(id),
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_activities_company_id ON activities (company_id);
CREATE INDEX IF NOT EXISTS ix_activities_company_status ON activities (company_id, status);

CREATE TABLE IF NOT EXISTS automations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    name TEXT NOT NULL,
    event_type INTEGER NOT NULL,
    condition_json TEXT NOT NULL,
    action_json TEXT NOT NULL,
    is_active BOOLEAN NOT NULL,
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_automations_company_id ON automations (company_id);
CREATE INDEX IF NOT EXISTS ix_automations_company_event ON automations (company_id, event_type);

CREATE TABLE IF NOT EXISTS event_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    type INTEGER NOT NULL,
    data_json TEXT NOT NULL,
    occurred_at_utc TIMESTAMPTZ NOT NULL,
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_event_logs_company_id ON event_logs (company_id);
CREATE INDEX IF NOT EXISTS ix_event_logs_company_type ON event_logs (company_id, type);

CREATE TABLE IF NOT EXISTS refresh_tokens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    user_id BIGINT NOT NULL REFERENCES users(id),
    token VARCHAR(200) NOT NULL UNIQUE,
    expires_at_utc TIMESTAMPTZ NOT NULL,
    is_revoked BOOLEAN NOT NULL,
    created_at_utc TIMESTAMPTZ NOT NULL,
    updated_at_utc TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_refresh_tokens_company_id ON refresh_tokens (company_id);

